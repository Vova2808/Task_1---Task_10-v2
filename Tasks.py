# Python вхождение подстроки в строку
def Task_9():
    str_x = "Вася хороший программист. Будь как Вася"
    count = 0
    xstr = str_x.split()
    for i in xstr:
        if i == 'Вася':
            count = count + 1
    print(f'Вася появился в строке {count} раз')

